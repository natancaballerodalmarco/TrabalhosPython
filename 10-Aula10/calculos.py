def calculo_soma(n1, n2):
    soma = n1 + n2
    return soma

def calculo_sub(n1, n2):
    return n1 - n2

def calculo_mult(n1, n2):
    return n1 * n2

def calculo_divint(n1, n2):
    return n1 // n2
    
def calculo_divfrac(n1, n2):
    return n1 / n2

def calculo_restodiv(n1, n2):
    return n1 % n2

def calculo_raiz(n1, n2):
    return  n1 ** (1/n2)
